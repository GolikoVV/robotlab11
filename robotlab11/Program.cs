using RobotEvent;
using robotlab11;

class Program
{
    static void Main(string[] args)
    {
        Robot robot = new Robot();
        BackListener listener = new BackListener();

        // Подписываем слушателя на событие робота
        robot.Back += listener.OnBack;

        // Запускаем поток выбора направления робота
        Thread thread = new Thread(new ThreadStart(robot.ChooseDirection));
        thread.Start();
    }
}

/*В этом примере класс Robot представляет робота, который выбирает случайное направление 
 * движения и вызывает событие Back, если выбрано направление "назад". 
 * Класс BackListener представляет слушателя события, который записывает 
 * сообщение в файл, если робот выбрал направление "назад". В методе Main 
 * создаются объекты робота и слушателя, слушатель подписывается на событие робота, 
 * и запускается поток выбора направления робота. Когда робот выбирает направление "назад", 
 * событие вызывается, и слушатель записывает сообщение в файл.
 */